//go:embed acronyms*.txt
var acronymsFS embed.FS
var acronyms = sync.OnceValue(func() map[string]string {
	entries, err := acronymsFS.ReadDir(".")
	if err != nil {
		fmt.Println("listing acronym files:", err)
		os.Exit(1)
	}

	m := make(map[string]string)
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}

		file := entry.Name()
		data, err := acronymsFS.ReadFile(file)
		if err != nil {
			fmt.Println("reading acronym file:", err)
			os.Exit(1)
		}

		for line := range strings.SplitSeq(string(data), "\n") {
			line = strings.TrimSpace(line)
			if line == "" || strings.HasPrefix(line, "//") {
				continue
			}

			lower := strings.ToLower(line)
			if _, ok := m[lower]; ok {
				fmt.Printf("duplicate acronym in file %s: %s\n", file, line)
				os.Exit(1)
			}
			m[lower] = line
		}
	}

	return m
})

func stylize(s string) string {
	if v, ok := acronyms()[strings.ToLower(s)]; ok {
		return v
	}
	return s
}

// camelCase converts a string like "foo_bar" to "FooBar". It capitalizes the
// acronyms defined in acronyms.txt.
func camelCase(s string) string {
	var b strings.Builder
	for w := range strings.SplitSeq(s, "_") {
		w = stylize(w)
		b.WriteString(cases.Title(language.English, cases.NoLower).String(w))
	}
	return b.String()
}

// glob expands the given glob patterns into a sequence of matching file paths.
func glob(patterns []string) iter.Seq[string] {
	var matches []string
	for _, pattern := range patterns {
		m, err := filepath.Glob(pattern)
		if err != nil {
			panic(fmt.Sprintf("invalid glob pattern %q: %v", pattern, err))
		}
		matches = append(matches, m...)
	}

	return slices.Values(matches)
}

// writeHeader writes a standard Go file header with the given package name and
// imports.
func writeHeader(w io.StringWriter, pkg string, imports []string) error {
	if err := writeCopyrightHeader(w); err != nil {
		return fmt.Errorf("writing copyright header: %w", err)
	}
	if err := writePackageHeader(w, pkg); err != nil {
		return fmt.Errorf("writing package header: %w", err)
	}
	if err := writeImports(w, imports); err != nil {
		return fmt.Errorf("writing imports: %w", err)
	}
	return nil
}

// writeCopyrightHeader writes the standard copyright header to the given
// writer.
func writeCopyrightHeader(w io.StringWriter) error {
	_, err := w.WriteString(`// Code generated by dpgen. DO NOT EDIT.

// SPDX-License-Identifier: Apache-2.0
// Copyright Authors of Cilium

`)

	return err
}

// writePackageHeader writes the package declaration to the given writer.
func writePackageHeader(w io.StringWriter, pkg string) error {
	if pkg == "" {
		return fmt.Errorf("package name cannot be empty")
	}
	_, err := w.WriteString(fmt.Sprintf("package %s\n\n", pkg))
	return err
}

func writeImports(w io.StringWriter, imports []string) error {
	if len(imports) == 0 {
		return nil
	}

	if len(imports) == 1 {
		_, err := w.WriteString(fmt.Sprintf("import %q\n\n", imports[0]))
		return err
	}

	_, err := w.WriteString("import (\n")
	if err != nil {
		return err
	}

	for _, imp := range imports {
		_, err := w.WriteString(fmt.Sprintf("\t%q\n", imp))
		if err != nil {
			return err
		}
	}

	_, err = w.WriteString(")\n\n")
	return err
}

// sorted returns the values of m as a slice, sorted by key.
func sorted[T any](m map[string]T) []T {
	keys := slices.Sorted(maps.Keys(m))
	out := make([]T, 0, len(m))
	for _, k := range keys {
		out = append(out, m[k])
	}
	return out
}

// sortedSeq iterates seq in sorted order.
func sortedSeq[T cmp.Ordered](seq iter.Seq[T]) iter.Seq[T] {
	return slices.Values(slices.Sorted(seq))
}

// mapSpecCompatible checks whether two MapSpecs have the same Type, KeySize,
// ValueSize, and MaxEntries. It returns an error if they are not compatible.
func mapSpecCompatible(a, b *ebpf.MapSpec) error {
	if a.Type != b.Type {
		return fmt.Errorf("type mismatch: %s != %s", a.Type, b.Type)
	}

	if a.KeySize != b.KeySize {
		return fmt.Errorf("key size mismatch: %d != %d", a.KeySize, b.KeySize)
	}

	if a.ValueSize != b.ValueSize {
		return fmt.Errorf("value size mismatch: %d != %d", a.ValueSize, b.ValueSize)
	}

	if a.MaxEntries != b.MaxEntries {
		return fmt.Errorf("max entries mismatch: %d != %d", a.MaxEntries, b.MaxEntries)
	}

	return nil
}

// addMapKV adds the BTF types of the map's key and value to the given BTF
// builder.
func addMapKV(bb *btf.Builder, added *set.Set[string], spec *ebpf.MapSpec) error {
	if spec.Key != nil {
		if err := addType(bb, added, spec.Key); err != nil {
			return fmt.Errorf("adding key type: %w", err)
		}
	}
	if spec.Value != nil {
		if err := addType(bb, added, spec.Value); err != nil {
			return fmt.Errorf("adding value type: %w", err)
		}
	}
	return nil
}

// addVariableType adds the BTF type of the variable to the given BTF builder.
func addVariableType(bb *btf.Builder, added *set.Set[string], v *ebpf.VariableSpec) error {
	if v.Type == nil || v.Type.Type == nil {
		return fmt.Errorf("no BTF type information")
	}

	return addType(bb, added, v.Type.Type)
}

func addType(bb *btf.Builder, added *set.Set[string], typ btf.Type) error {
	name := btf.UnderlyingType(typ).TypeName()
	if td, ok := btf.As[*btf.Typedef](typ); ok {
		name = td.TypeName()
	}
	if name == "" {
		return fmt.Errorf("anonymous type %v", typ)
	}

	// Always add the type chain in full. The underlying type will be queryable by
	// name, as well as any typedefs referring to it.
	if _, err := bb.Add(typ); err != nil {
		return fmt.Errorf("adding type %s: %w", name, err)
	}

	// If the incoming type is a typedef, emit the typedef, not only the
	// underlying type. Typically, the typedef will be the one used in the map
	// declaration, so it needs to be queryable by name in the resulting BTF blob.
	if added != nil {
		added.Insert(name)
	}
	return nil
}

// bpfFlagsToString converts BPF map flags into a string representation
// using unix.BPF_F_* constant names where possible.
func bpfFlagsToString(flags uint32) string {
	// The only map flags used by Cilium at time of writing.
	flagNames := map[uint32]string{
		unix.BPF_F_NO_PREALLOC:   "unix.BPF_F_NO_PREALLOC",
		unix.BPF_F_NO_COMMON_LRU: "unix.BPF_F_NO_COMMON_LRU",
		unix.BPF_F_RDONLY_PROG:   "unix.BPF_F_RDONLY_PROG",
	}

	var consts []string
	for i := range 32 {
		if flag := flags & (1 << i); flag != 0 {
			flagStr, ok := flagNames[flag]
			if ok {
				consts = append(consts, flagStr)
			} else {
				// Fallback: print the hex value if we don't have a string translation.
				consts = append(consts, fmt.Sprintf("0x%x", flag))
			}
		}
	}

	if len(consts) == 0 {
		consts = append(consts, "0")
	}

	return strings.Join(consts, " | ")
}
