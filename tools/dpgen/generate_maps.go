func LoadMapSpecs() (map[string]*ebpf.MapSpec, error) {
	btfSpec, err := btf.LoadSpecFromReader(bytes.NewReader(mapBtf))
	if err != nil {
		return nil, err
	}

	mapSpecs := make(map[string]*ebpf.MapSpec)
	for _, constructor := range constructors {
		spec, err := constructor(btfSpec)
		if err != nil {
			return nil, err
		}
		mapSpecs[spec.Name] = spec
	}

	return mapSpecs, nil
}

