func TestSampleRing(t *testing.T) {
	var r sampleRing
	var bm SampleBitmap
	inputs := []float32{
		1.0, 0.0, 3.0, 4.0, 0.0, 5.0,
	}
	for _, in := range inputs {
		r.push(in)
		bm.mark(in != 0)
	}
	samples := r.grab()
	slices.Reverse(inputs)
	assert.Equal(t, inputs, samples[:len(inputs)])
	for i := range inputs {
		assert.Equal(t, inputs[i] != 0.0, bm.exists(i))
	}
}

